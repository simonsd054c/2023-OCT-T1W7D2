# Input numerator as n and denominator as d
n = int(input("Enter numerator: "))
d = int(input("Enter denominator: "))

# if d == 0
if d == 0:
    # display "Denominator cannot be zero" as output
    print("Denominator cannot be zero")

# else
else:
    # q = n / d
    q = n / d
    # display q
    print(q)